/* this function returns the number of rows in a file */
#include "sort_animation.h"

int num_of_rows(FILE *fp) {
	int cnt;
	char c;
	while((c = getc(fp)) != EOF) {
		if(c == '\n')
			cnt++;
	}
	return cnt;
}

